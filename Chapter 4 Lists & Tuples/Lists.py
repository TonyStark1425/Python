# Lists: A built-in data type that stores set of values
# It can store elements of different types (integer, float, string, etc.)
# strings are immutable but lists are mutable

marks = [96.5, "Stark", 89.9, 77.7]
print(marks)
print(type(marks))
print(len(marks))
marks[0] = "Tony"
print(marks[0]) 
print(marks[3])