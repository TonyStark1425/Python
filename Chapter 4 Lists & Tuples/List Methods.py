list = [2, 1, 3]

list.append(4) #adds one element at the end
print(list)

list.reverse() #reverse list
print(list)

list.sort() #sorts in ascending order
print(list)

list.sort(reverse=True) #sorts in decending order
print(list)

list.insert(0, 7) #insert element at index
print(list)

list.remove(2) #removes first occurence of element
print(list)

list.pop(2) #removes element at idx
print(list)